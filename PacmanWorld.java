    public void addScore(int points)
    {
        score = score + points;
        if (scoreBoard != null) {
            scoreBoard.updateScore(score); 
        }

        if (score >= nextFruitScore)
        {
            spawnFruit();
            nextFruitScore += 200;
        }
        
        checkHighScore();
    }
    
    private void spawnFruit()
    {
        if (getObjects(Fruit.class).isEmpty()) 
        {
            int[][] safePositions = {
                {150, 59},   
                {300, 59}, 
                {60, 268}, 
                {400, 268}, 
                {70, 506}, 
                {380, 506}, 
                {225, 217},
                {225, 314} 
            };

            int index = random.nextInt(safePositions.length);
            
            int spawnX = safePositions[index][0];
            int spawnY = safePositions[index][1];
            
            addObject(new Fruit(), spawnX, spawnY); 
        }
    }

    /**
     * Menyiapkan latar belakang labirin.   
     */
    private void prepareBackground()
    {
        GreenfootImage bg = getBackground();
        bg.setColor(Color.BLACK);
        bg.fill();

        GreenfootImage bgMaze = new GreenfootImage("wall_map_background.png");

        bgMaze.scale(bgMaze.getWidth() * scaleFactor, bgMaze.getHeight() * scaleFactor);

        bg.drawImage(bgMaze, 0, 32); 
    }
  
    /**
     * Menampilkan skor saat ini di layar
     */
    private void showScore() {
        removeObjects(getObjects(DigitActor.class));
        
        List<TextActor> oldScoreLabels = getObjects(TextActor.class);
        for (TextActor label : oldScoreLabels) {
            if (label.getY() > 500) { 
                removeObject(label);
            }
        }
        
        removeObjects(getObjects(ScoreBoard.class)); 

        String scoreLabel = "SCORE"; 
        int labelStartX = 40;     
        int yPos = 540;           
        int labelSpacing = 16;     
        int scoreStartX = labelStartX + (scoreLabel.length() * labelSpacing) + 8;
        int digitSpacing = 0; 

        for (int i = 0; i < scoreLabel.length(); i++) {
             char character = scoreLabel.charAt(i);
             String filename = "char_" + Character.toLowerCase(character) + ".png";
             try {
                 TextActor letter = new TextActor(filename);
                 addObject(letter, labelStartX + (i * labelSpacing), yPos);
             } catch (IllegalArgumentException e) { System.err.println("Error: File aset huruf '" + filename + "' tidak ditemukan!"); }
        }

        String scoreString = "" + score;
        for (int i = 0; i < scoreString.length(); i++) {
            int digitValue = Character.getNumericValue(scoreString.charAt(i));
            DigitActor digit = new DigitActor(digitValue);
            
            if(i == 0 && digit.getDigitWidth() > 0) {
                 digitSpacing = digit.getDigitWidth() + 2;
            } else if (digitSpacing == 0) {
                 digitSpacing = 16 + 2;
            }
            int x = scoreStartX + (i * digitSpacing);
            addObject(digit, x, yPos);
        }
    }
    
    private void checkHighScore() {
        int currentHighScore = StartScreen.getHighScore(); 

        if (score > currentHighScore) {
            StartScreen.highScore = score; 
            saveHighScore(); 
        }
    }
     
    private void saveHighScore() {
        if (UserInfo.isStorageAvailable()) {
            UserInfo myInfo = UserInfo.getMyInfo();
            if (myInfo != null) {
                 myInfo.setScore(score); 
                 myInfo.store(); 
            } else {
                 System.out.println("UserInfo tidak tersedia, tidak bisa menyimpan high score.");
            }
        } else {
            System.out.println("Penyimpanan UserInfo tidak tersedia.");
        }
    }
    
